#!/usr/bin/env node
/**
 * generate-wi-tokens.mjs — Emit CSS custom properties from shared/warm-instrument/tokens.json
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "../..");
const TOKENS_PATH = path.join(REPO_ROOT, "shared/warm-instrument/tokens.json");
const OUT_PATH = path.join(
  REPO_ROOT,
  "ui/client/src/components/home-warm/wi-tokens.generated.css",
);

const tokens = JSON.parse(fs.readFileSync(TOKENS_PATH, "utf-8"));

const sportVars = Object.entries(tokens.sports)
  .map(([id, sport]) => {
    const cssId = id === "cycling" ? "cycling" : id.replace(/_/g, "-");
    const wiKey = id === "weight_training" ? "weights" : cssId;
    if (["badminton", "calisthenics", "foundation", "cycling"].includes(id)) {
      return `  --wi-${wiKey}: ${sport.hex};`;
    }
    return null;
  })
  .filter(Boolean)
  .join("\n");

const css = `/* Generated by ui/scripts/generate-wi-tokens.mjs — do not edit by hand */
.wi-shell {
  --wi-page: ${tokens.palette.desk};
  --wi-surface: ${tokens.palette.paper};
  --wi-surface-muted: ${tokens.palette.surfaceMuted};
  --wi-ink: ${tokens.palette.ink};
  --wi-ink-muted: ${tokens.palette.inkMuted};
  --wi-ink-faint: ${tokens.palette.inkFaint};
  --wi-line: ${tokens.lines.border};
  --wi-line-dashed: ${tokens.lines.borderDashed};
  --wi-rust: ${tokens.palette.accent};
  --wi-rust-dark: ${tokens.palette.accentDark};
${sportVars}
  --wi-card-shadow: ${tokens.shadows.card};
  --wi-engine-shadow: ${tokens.shadows.engine};
}
`;

fs.writeFileSync(OUT_PATH, css);
console.log("✓ wi-tokens.generated.css written");
